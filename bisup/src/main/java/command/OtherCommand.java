package command;
//부수비용관리
public class OtherCommand {

	private String id; //아이디
	private String mon;//월
	private int rent;//월세
	private int sal;//월급
	private int mcost;//관리비
	private int duty;//세금
	private int prcost;//홍보비
	private int etcco;//총부수비용
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMon() {
		return mon;
	}
	public void setMon(String mon) {
		this.mon = mon;
	}
	public int getRent() {
		return rent;
	}
	public void setRent(int rent) {
		this.rent = rent;
	}
	public int getSal() {
		return sal;
	}
	public void setSal(int sal) {
		this.sal = sal;
	}
	public int getMcost() {
		return mcost;
	}
	public void setMcost(int mcost) {
		this.mcost = mcost;
	}
	public int getDuty() {
		return duty;
	}
	public void setDuty(int duty) {
		this.duty = duty;
	}
	public int getPrcost() {
		return prcost;
	}
	public void setPrcost(int prcost) {
		this.prcost = prcost;
	}
	public int getEtcco() {
		return etcco;
	}
	public void setEtcco(int etcco) {
		this.etcco = etcco;
	}
	
	
}
